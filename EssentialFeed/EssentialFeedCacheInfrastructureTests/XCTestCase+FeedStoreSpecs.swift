extension FeedStoreSpecs where Self: XCTestCase {
    
    func expect(_ sut: FeedStore, toRetrieveTwice expectedResult: FeedStore.RetrievalResult,
                file: StaticString = #filePath, line: UInt = #line)  {
        expect(sut, toRetrieve: expectedResult, file: file, line: line)
        expect(sut, toRetrieve: expectedResult, file: file, line: line)
    }
    
    func expect(_ sut: FeedStore, toRetrieve expectedResult: FeedStore.RetrievalResult,
                file: StaticString = #filePath, line: UInt = #line) {
        let retrievedResult = Result { try sut.retrieve() }
        switch (expectedResult, retrievedResult) {
        case (.success(.none), .success(.none)),
             (.failure, .failure):
            break
        case let (.success(expectedFeed), .success(retrievedFeed)):
            XCTAssertEqual(retrievedFeed?.feed, expectedFeed?.feed, file: file, line: line)
            XCTAssertEqual(retrievedFeed?.timestamp, expectedFeed?.timestamp, file: file, line: line)
            
        default:
            XCTFail("Expected to retrieve \(expectedResult), got \(retrievedResult) instead", file: file, line: line)
        }
    }
    
    @discardableResult
    func insert(_ cache: (feed: [LocalFeedImage], timestamp: Date), to sut: FeedStore) -> Error? {
        var insertionError: Error?
        do {
            try sut.insert(cache.feed, timestamp: cache.timestamp)
        } catch {
           insertionError = error
        }
        return insertionError
    }
    
    @discardableResult
    func deleteCache(from sut: FeedStore) -> Error? {
        var deletionError: Error?
        do {
            try sut.deleteCachedFeed()
        } catch {
            deletionError = error
        }
        return deletionError
    }
}
